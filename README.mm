## 💻 التطبيق البرمجي (Implementation)

تم توفير الدالة البرمجية الرسمية المعتمدة للحساب بثلاث لغات برمجية أساسية:

### 1️⃣ لغة بايثون (Python)
```python
import math

def albadai_digit_permutation(n, r):
    """
    حساب عدد طرق تكوين أعداد من (r) خانة من مجموعة أرقام عددها (n)
    بناءً على قانون البداي للاختزال الرقمي المباشر.
    """
    if r > n or n <= 0 or r <= 0:
        return 0
    
    # حساب التباديل التقليدية ثم تطبيق صيغة البداي الاختزالية
    p_n_r = math.perm(n, r)
    return ((n - 1) * p_n_r) // n

# مثال تطبيقي للمجموعة العشرية (10 أرقام) للحصول على رقم من 3 خانات
print(albadai_digit_permutation(10, 3))  # المخرج: 648
```

### 2️⃣ لغة سي بلس بلس (C++)
```cpp
#include <iostream>

class AlBadaiMath {
public:
    static long long getDigitPermutations(int n, int r) {
        if (r > n || n <= 0 || r <= 0) return 0;

        // حساب التباديل P(n, r)
        long long p_n_r = 1;
        for (int i = 0; i < r; i++) {
            p_n_r *= (n - i);
        }

        // تطبيق معادلة البداي للاختزال الكلي
        return ((n - 1) * p_n_r) / n;
    }
};

int main() {
    std::cout << AlBadaiMath::getDigitPermutations(10, 3) << std::endl; // المخرج: 648
    return 0;
}
```

### 3️⃣ لغة جافا سكريبت (JavaScript)
```javascript
/**
 * حساب تباديل الأرقام باستبعاد الصفر تلقائياً
 * ابتكار: محمد البداي
 */
function alBadaiDigitPermutation(n, r) {
    if (r > n || n <= 0 || r <= 0) return 0;

    let p_n_r = 1;
    for (let i = 0; i < r; i++) {
        p_n_r *= (n - i);
    }

    return Math.floor(((n - 1) * p_n_r) / n);
}

console.log(alBadaiDigitPermutation(10, 3)); // المخرج: 648
```


# 📐 قاعدة البدّاي لتباديل الأرقام (Al-Badai Digit-Permutation Rule)

هذا المستودع البرمجي مخصص لتوثيق وتطبيق الخوارزمية الرياضية المبتكرة من قِبل **محمد البدّاي (Muhammad Albaddai)**، والمندرجة ضمن مشروعه الهندسي الرياضي "هندسة التناخم".

## 💡 نظرة عامة (Overview)
تقدم هذه القاعدة حلاً رياضياً مختصراً ومباشراً لحساب عدد طرق تكوين أعداد من بين مجموعة أرقام معينة، مع **استبعاد الحالات التي تبدأ بالصفر تلقائياً** (لأن الصفر على يسار الأعداد الصحيحة ليس له قيمة منطقية ويقلل من رتبة العدد). تكمن ميزة هذا الابتكار في إلغاء الحاجة لبناء شروط منطقية (If Conditions) أو تكرارية معقدة عند البرمجة، واستبدالها بمعادلة رياضية اختزالية موحدة.

## 📐 القانون الرياضي (The Formula)
بدلاً من الحساب اليدوي لكل خانة وتفصيل خياراتها، تعتمد الصيغة على الاختزال الكلي التالي:

\[\text{Result} = \frac{(n - 1) \times P(n, r)}{n}\]

حيث أن:
* **\(n\)**: العدد الكلي للأرقام المتاحة في المجموعة (مثال: 10 أرقام للنظام العشري).
* **\(r\)**: عدد الخانات المطلوب تشكيل العدد منها (مثال: تكوين رقم من 3 خانات).
* **\(P(n, r)\)**: قانون التباديل التقليدي لحساب عدد الاحتمالات المتاحة بدون قيود.

